package IfElse;

public class ifElse {

	public static void main(String[] args) {

		int x=92;
		int y=89;
		
		//condition 1
		if(x>y)
		{
			System.out.println("correct");
		}
		else {
			System.out.println("incorrect");
		}
		
		//condition 2
		if (x<y)
		{
			System.out.println("correct");
		}
		else {
			System.out.println("incorrect");
		}
		
		//condition 3
		if (x==y) {
			System.out.println("correct");
		}
		else {
			System.out.println("incorrect");
		}
		
		//condition 4
		if (x!=y) {
			System.out.println("correct");
		}
		else {
			System.out.println("incorrect");
		}
	}
	
	
}
